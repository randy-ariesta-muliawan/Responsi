# Casting ke string
a = "abc"
b = 23
c = 3.5

# str()
b = str(b)
c = str(c)
print(a+b+c)

# Casting ke integer
a = "12"
b = 23
c = 3.5

# int()
a = int(a)
b = int(b)
c = int(c)
print(a+b+c)

# Casting ke float
a = "12"
b = 23
c = 3.5
# float()
a = float(a)
b = float(b)
c = float(c)
print(a+b+c)

