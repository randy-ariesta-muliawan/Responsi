angka = 1
jumlah = 0
print("Masukkan angka baru ke dalam jumlah !")
print("Masukkan angka 0 untuk keluar")
while angka != 0 :
	print("Jumlah angka sekarang", jumlah)
	angka = float(input("Masukkan angka : "))
	jumlah += angka
print("Jumlah angka = ", jumlah)

