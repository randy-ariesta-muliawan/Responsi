"""
Boolean :
- True (benar)
- False (salah)

Logic Gate :
- and (dan)
- or (atau)
- not (tidak)
"""

print("True dan True", True and True)
print("True dan False", True and False)
print("False dan True", False and True)
print("False dan False", False and False)

print("True atau True", True or True)
print("True atau False", True or False)
print("False atau True", False or True)
print("False atau False", False or False)

print("Not True :", not True)
print("Not False : ", not False)